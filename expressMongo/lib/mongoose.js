var config = require('config-lite');
var mongoose = require('mongoose');
mongoose.connect(config.mongodb);

exports.User = mongoose.model('User', {
  name: { type: 'string' },
  password: { type: 'string' },
  avatar: { type: 'string' },
  gender: { type: 'string', enum: ['m', 'f', 'x'] },
  bio: { type: 'string' }
});